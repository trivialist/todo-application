    public void deleteFinStatus(int statusID) {
        DB_ToDo_Connect dbCon = new DB_ToDo_Connect();
        dbCon.openDB();
        con = dbCon.getCon();
        try {
            Statement stmt = con.createStatement();
            String sql = "DELETE FROM Status WHERE StatusID=" + statusID;
            stmt.executeUpdate(sql);
            stmt.close();
        }
        catch(Exception ex) {
            Logger.getLogger(DeleteFinStatusDialog.class.getName()).log(Level.SEVERE, null, ex);
            System.exit(1); 
        }
        dbCon.closeDB(con);
    }
    
